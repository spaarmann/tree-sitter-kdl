static bool ts_lex(TSLexer *lexer, TSStateId state) {
  START_LEXER();
  eof = lexer->eof(lexer);
  switch (state) {
    case 0:
      if (eof) ADVANCE(7);
      if (lookahead == '\n') ADVANCE(28);
      if (lookahead == ' ') ADVANCE(30);
      if (lookahead == '+') ADVANCE(17);
      if (lookahead == '-') ADVANCE(18);
      if (lookahead == '.') ADVANCE(11);
      if (lookahead == '0') ADVANCE(26);
      if (lookahead == '1') ADVANCE(27);
      if (lookahead == ';') ADVANCE(8);
      if (lookahead == '=') ADVANCE(9);
      if (lookahead == 'E') ADVANCE(13);
      if (lookahead == '\\') ADVANCE(31);
      if (lookahead == '_') ADVANCE(14);
      if (lookahead == 'e') ADVANCE(12);
      if (lookahead == 65279) ADVANCE(29);
      if (lookahead == '8' ||
          lookahead == '9') ADVANCE(15);
      if (('A' <= lookahead && lookahead <= 'F')) ADVANCE(20);
      if (('a' <= lookahead && lookahead <= 'f')) ADVANCE(10);
      if (('2' <= lookahead && lookahead <= '7')) ADVANCE(15);
      if (('g' <= lookahead && lookahead <= 'z')) ADVANCE(10);
      END_STATE();
    case 1:
      if (lookahead == '\n') ADVANCE(28);
      if (lookahead == ' ') ADVANCE(30);
      if (lookahead == '.') ADVANCE(11);
      if (lookahead == ';') ADVANCE(8);
      if (lookahead == 'E') ADVANCE(13);
      if (lookahead == '\\') ADVANCE(31);
      if (lookahead == '_') ADVANCE(14);
      if (lookahead == 'e') ADVANCE(12);
      if (lookahead == 65279) ADVANCE(29);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(15);
      END_STATE();
    case 2:
      if (lookahead == '\n') ADVANCE(28);
      if (lookahead == ' ') ADVANCE(30);
      if (lookahead == '0') ADVANCE(25);
      if (lookahead == '1') ADVANCE(27);
      if (lookahead == ';') ADVANCE(8);
      if (lookahead == '\\') ADVANCE(31);
      if (lookahead == '_') ADVANCE(14);
      if (lookahead == 65279) ADVANCE(29);
      END_STATE();
    case 3:
      if (lookahead == '\n') ADVANCE(28);
      if (lookahead == ' ') ADVANCE(30);
      if (lookahead == ';') ADVANCE(8);
      if (lookahead == '\\') ADVANCE(31);
      if (lookahead == '_') ADVANCE(14);
      if (lookahead == 65279) ADVANCE(29);
      if (('0' <= lookahead && lookahead <= '9') ||
          ('A' <= lookahead && lookahead <= 'F') ||
          ('a' <= lookahead && lookahead <= 'f')) ADVANCE(20);
      END_STATE();
    case 4:
      if (lookahead == '\n') ADVANCE(28);
      if (lookahead == ' ') ADVANCE(30);
      if (lookahead == ';') ADVANCE(8);
      if (lookahead == '\\') ADVANCE(31);
      if (lookahead == 65279) ADVANCE(29);
      if (('0' <= lookahead && lookahead <= '7') ||
          lookahead == '_') ADVANCE(23);
      END_STATE();
    case 5:
      if (('0' <= lookahead && lookahead <= '7')) ADVANCE(22);
      END_STATE();
    case 6:
      if (eof) ADVANCE(7);
      if (lookahead == '\n') ADVANCE(28);
      if (lookahead == ' ') ADVANCE(30);
      if (lookahead == '+') ADVANCE(17);
      if (lookahead == '-') ADVANCE(18);
      if (lookahead == '0') ADVANCE(16);
      if (lookahead == ';') ADVANCE(8);
      if (lookahead == '=') ADVANCE(9);
      if (lookahead == '\\') ADVANCE(31);
      if (lookahead == 65279) ADVANCE(29);
      if (('1' <= lookahead && lookahead <= '9')) ADVANCE(15);
      if (('a' <= lookahead && lookahead <= 'z')) ADVANCE(10);
      END_STATE();
    case 7:
      ACCEPT_TOKEN(ts_builtin_sym_end);
      END_STATE();
    case 8:
      ACCEPT_TOKEN(anon_sym_SEMI);
      END_STATE();
    case 9:
      ACCEPT_TOKEN(anon_sym_EQ);
      END_STATE();
    case 10:
      ACCEPT_TOKEN(aux_sym_identifier_token1);
      END_STATE();
    case 11:
      ACCEPT_TOKEN(anon_sym_DOT);
      END_STATE();
    case 12:
      ACCEPT_TOKEN(anon_sym_e);
      END_STATE();
    case 13:
      ACCEPT_TOKEN(anon_sym_E);
      END_STATE();
    case 14:
      ACCEPT_TOKEN(anon_sym__);
      END_STATE();
    case 15:
      ACCEPT_TOKEN(sym__digit);
      END_STATE();
    case 16:
      ACCEPT_TOKEN(sym__digit);
      if (lookahead == 'b') ADVANCE(24);
      if (lookahead == 'o') ADVANCE(21);
      if (lookahead == 'x') ADVANCE(19);
      END_STATE();
    case 17:
      ACCEPT_TOKEN(anon_sym_PLUS);
      END_STATE();
    case 18:
      ACCEPT_TOKEN(anon_sym_DASH);
      END_STATE();
    case 19:
      ACCEPT_TOKEN(anon_sym_0x);
      END_STATE();
    case 20:
      ACCEPT_TOKEN(sym__hex_digit);
      END_STATE();
    case 21:
      ACCEPT_TOKEN(anon_sym_0o);
      END_STATE();
    case 22:
      ACCEPT_TOKEN(aux_sym__octal_token1);
      END_STATE();
    case 23:
      ACCEPT_TOKEN(aux_sym__octal_token2);
      END_STATE();
    case 24:
      ACCEPT_TOKEN(anon_sym_0b);
      END_STATE();
    case 25:
      ACCEPT_TOKEN(anon_sym_0);
      END_STATE();
    case 26:
      ACCEPT_TOKEN(anon_sym_0);
      if (lookahead == 'o') ADVANCE(21);
      if (lookahead == 'x') ADVANCE(19);
      END_STATE();
    case 27:
      ACCEPT_TOKEN(anon_sym_1);
      END_STATE();
    case 28:
      ACCEPT_TOKEN(sym__newline);
      END_STATE();
    case 29:
      ACCEPT_TOKEN(anon_sym_);
      END_STATE();
    case 30:
      ACCEPT_TOKEN(sym__unicode_space);
      END_STATE();
    case 31:
      ACCEPT_TOKEN(anon_sym_BSLASH);
      END_STATE();
    default:
      return false;
  }
}

