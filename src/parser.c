      if (lookahead == 'n') ADVANCE(20);
      if (lookahead == 't') ADVANCE(19);
      if (lookahead == 65279) ADVANCE(42);
      if (lookahead == '8' ||
          lookahead == '9') ADVANCE(25);
      if (('A' <= lookahead && lookahead <= 'F')) ADVANCE(30);
      if (('a' <= lookahead && lookahead <= 'f')) ADVANCE(17);
      if (('2' <= lookahead && lookahead <= '7')) ADVANCE(25);
      if (('g' <= lookahead && lookahead <= 'z')) ADVANCE(17);
      END_STATE();
