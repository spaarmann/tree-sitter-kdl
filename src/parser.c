      END_STATE();
    case 2:
      if (lookahead == '\n') ADVANCE(41);
      if (lookahead == ' ') ADVANCE(43);
      if (lookahead == '.') ADVANCE(21);
      if (lookahead == ';') ADVANCE(15);
      if (lookahead == 'E') ADVANCE(23);
